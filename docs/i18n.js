let currentLang = (typeof localStorage !== "undefined" && localStorage.getItem("ctai_lang")) || "ja";

function t(key) {
  const dict = I18N[currentLang] || I18N.ja;
  return dict[key] !== undefined ? dict[key] : (I18N.ja[key] !== undefined ? I18N.ja[key] : key);
}

// {paramName} をparams内の値に置き換えた文言を返す(動的なステータス/エラーメッセージ用)。
function tf(key, params) {
  let s = t(key);
  for (const [name, value] of Object.entries(params || {})) {
    s = s.split(`{${name}}`).join(value);
  }
  return s;
}

function applyI18n() {
  document.documentElement.lang = currentLang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.getAttribute("data-i18n"));
  });
  document.querySelectorAll("[data-i18n-html]").forEach((el) => {
    el.innerHTML = t(el.getAttribute("data-i18n-html"));
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.getAttribute("data-i18n-placeholder"));
  });

  document.querySelectorAll(".lang-switch-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === currentLang);
  });

  document.dispatchEvent(new CustomEvent("i18n:changed"));
}

function setLang(lang) {
  if (!I18N[lang]) return;
  currentLang = lang;
  try {
    localStorage.setItem("ctai_lang", lang);
  } catch (err) {
    // localStorageが使えない環境(プライベートモード等)では単に保存をスキップする
  }
  applyI18n();
}

document.addEventListener("DOMContentLoaded", () => {
  applyI18n();
  document.querySelectorAll(".lang-switch-btn").forEach((btn) => {
    btn.addEventListener("click", () => setLang(btn.dataset.lang));
  });
});
